# 9d — Detrended Fluctuation Analysis (DFA)
# =====================================================================

def compute_dfa(pages: list[dict], min_window: int = 10,
                max_window: int = 2000, n_windows: int = 20) -> dict:
    """Detrended Fluctuation Analysis on character stream.

    Maps each character to (rank - mean_rank), creating a random-walk-like
    signal. Computes RMS fluctuation F(n) for window sizes n, then fits
    F(n) ~ n^alpha in log-log space.
    """
    chars = _extract_char_stream(pages)
    n = len(chars)

    # Map chars to numerical values (frequency rank, centered)
    char_freq = Counter(chars)
    ranked = sorted(char_freq.keys(), key=lambda c: -char_freq[c])
    char_to_val = {c: i for i, c in enumerate(ranked)}
    values = np.array([char_to_val[c] for c in chars], dtype=float)
    values -= values.mean()

    # Cumulative sum (random walk / profile)
    profile = np.cumsum(values)

    # Window sizes (logarithmically spaced)
    max_w = min(max_window, n // 4)
    if max_w < min_window:
        return {"alpha": None, "skipped": True, "reason": "corpus too small"}

    windows = np.unique(np.logspace(
        np.log10(min_window), np.log10(max_w), n_windows
    ).astype(int))
    windows = windows[windows >= min_window]

    fluctuations = []
    valid_windows = []

    for w in windows:
        n_segments = n // w
        if n_segments < 2:
            continue

        rms_list = []
        for seg in range(n_segments):
            start = seg * w
            end = start + w
            segment = profile[start:end]

            # Detrend: fit linear trend and subtract
            x = np.arange(w, dtype=float)
            coeffs = np.polyfit(x, segment, 1)
            trend = np.polyval(coeffs, x)
            residual = segment - trend
            rms_list.append(float(np.sqrt(np.mean(residual ** 2))))

        if rms_list:
            fluctuations.append(float(np.mean(rms_list)))
            valid_windows.append(int(w))

    if len(valid_windows) < 3:
        return {"alpha": None, "skipped": True, "reason": "too few valid windows"}

    # Fit log(F) vs log(n) => alpha
    log_w = np.log10(np.array(valid_windows, dtype=float))
    log_f = np.log10(np.array(fluctuations, dtype=float))
    alpha, intercept, r_value, _, _ = sp_stats.linregress(log_w, log_f)

    return {
        "alpha": round(float(alpha), 4),
        "alpha_r2": round(float(r_value ** 2), 4),
        "n_windows": len(valid_windows),
        "window_range": [valid_windows[0], valid_windows[-1]],
        "interpretation": _interpret_alpha(float(alpha)),
    }


def null_dfa(pages: list[dict], n_perms: int = 50,
             seed: int = SEED) -> dict:
    """Null: shuffle characters globally (destroying all structure),
    recompute DFA alpha. Uses fewer perms than other tests (DFA is expensive)."""
    rng = random.Random(seed)
    chars = _extract_char_stream(pages)

    char_freq = Counter(chars)
    ranked = sorted(char_freq.keys(), key=lambda c: -char_freq[c])
    char_to_val = {c: i for i, c in enumerate(ranked)}
    mean_val = np.mean([char_to_val[c] for c in chars])

    nulls = []
    for _ in range(n_perms):
        shuffled = list(chars)
        rng.shuffle(shuffled)

        values = np.array([char_to_val[c] for c in shuffled], dtype=float)
        values -= mean_val
        profile = np.cumsum(values)

        n = len(shuffled)
        windows = np.unique(np.logspace(
            np.log10(10), np.log10(min(2000, n // 4)), 20
        ).astype(int))
        windows = windows[windows >= 10]

        flucts, valid_w = [], []
        for w in windows:
            n_seg = n // w
            if n_seg < 2:
                continue
            rms_list = []
            for seg in range(n_seg):
                s = seg * w
                segment = profile[s:s + w]
                x = np.arange(w, dtype=float)
                coeffs = np.polyfit(x, segment, 1)
                trend = np.polyval(coeffs, x)
                residual = segment - trend
                rms_list.append(float(np.sqrt(np.mean(residual ** 2))))
            if rms_list:
                flucts.append(float(np.mean(rms_list)))
                valid_w.append(int(w))

        if len(valid_w) >= 3:
            log_w = np.log10(np.array(valid_w, dtype=float))
            log_f = np.log10(np.array(flucts, dtype=float))
            a, _, _, _, _ = sp_stats.linregress(log_w, log_f)
            nulls.append(float(a))

    if not nulls:
        return {"null_mean_alpha": None, "null_std_alpha": None}

    return {
        "null_mean_alpha": round(float(np.mean(nulls)), 4),
        "null_std_alpha": round(float(np.std(nulls, ddof=1)), 4),
    }


def _interpret_alpha(alpha: float) -> str:
    if alpha < 0.45:
        return "anti-correlated"
    elif alpha < 0.55:
        return "uncorrelated (white noise / shuffled)"
    elif alpha < 0.75:
        return "short-range correlated (typical natural language)"
    elif alpha < 1.0:
        return "long-range correlated"
    else:
        return "non-stationary / very strong correlations"


