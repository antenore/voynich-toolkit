# 9c — Zipf's laws (both)
# =====================================================================

def compute_zipf_laws(pages: list[dict]) -> dict:
    """Compute both Zipf's laws.

    First law: rank vs frequency (log-log slope ~ -1).
    Second law: frequency spectrum — number of types with frequency f.
    """
    all_words = _extract_all_words(pages)
    freq = Counter(all_words)

    # First law: rank-frequency
    sorted_freqs = sorted(freq.values(), reverse=True)
    ranks = np.log10(np.arange(1, len(sorted_freqs) + 1, dtype=float))
    freqs_log = np.log10(np.array(sorted_freqs, dtype=float))
    slope1, intercept1, r_value1, _, _ = sp_stats.linregress(ranks, freqs_log)

    # Second law: frequency spectrum
    # f(k) = number of word types with frequency k
    freq_spectrum = Counter(freq.values())
    ks = sorted(freq_spectrum.keys())
    counts = [freq_spectrum[k] for k in ks]

    # Fit power law to spectrum (log-log)
    if len(ks) >= 5:
        log_k = np.log10(np.array(ks, dtype=float))
        log_c = np.log10(np.array(counts, dtype=float))
        slope2, intercept2, r_value2, _, _ = sp_stats.linregress(log_k, log_c)
    else:
        slope2, r_value2 = float("nan"), float("nan")

    return {
        "first_law_slope": round(float(slope1), 4),
        "first_law_r2": round(float(r_value1 ** 2), 4),
        "second_law_slope": round(float(slope2), 4),
        "second_law_r2": round(float(r_value2 ** 2), 4),
        "n_types": len(freq),
        "n_tokens": len(all_words),
        "hapax": sum(1 for c in freq.values() if c == 1),
        "top10": [{"word": w, "freq": c}
                  for w, c in freq.most_common(10)],
    }


