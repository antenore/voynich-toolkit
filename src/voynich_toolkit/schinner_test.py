def compute_wordlen_distribution(pages: list[dict]) -> dict:
    """Compute word-length distribution and fit to binomial."""
    all_words = _extract_all_words(pages)
    lengths = [len(w) for w in all_words]

    mean_len = float(np.mean(lengths))
    std_len = float(np.std(lengths, ddof=1))
    max_len = max(lengths)

    # Frequency distribution
    len_freq = Counter(lengths)
    total = len(lengths)

    # Fit binomial B(n, p): mean = np, var = np(1-p)
    # Estimate n and p from mean and variance
    variance = float(np.var(lengths, ddof=1))
    if mean_len > 0 and variance > 0 and variance < mean_len:
        p_est = 1 - (variance / mean_len)
        n_est = mean_len / p_est
    else:
        # Fallback: use max_len as n
        n_est = float(max_len)
        p_est = mean_len / n_est if n_est > 0 else 0.5

    # Goodness-of-fit: chi-square test against binomial
    # Bin lengths 1..max_len
    observed = []
    expected = []
    for k in range(1, max_len + 1):
        obs = len_freq.get(k, 0)
        # Binomial probability P(X=k) for B(round(n_est), p_est)
        n_binom = max(1, round(n_est))
        if k <= n_binom:
            exp_prob = sp_stats.binom.pmf(k, n_binom, p_est)
        else:
            exp_prob = 0.0
        exp_count = exp_prob * total
        observed.append(obs)
        expected.append(exp_count)

    # Merge bins with expected < 5 from the tails
    obs_merged, exp_merged = _merge_small_bins(observed, expected, min_expected=5)

    # Normalise expected to match observed total (required by chisquare)
    obs_total = sum(obs_merged)
    exp_total = sum(exp_merged)
    if exp_total > 0:
        exp_merged = [e * obs_total / exp_total for e in exp_merged]

    if len(obs_merged) > 1:
        chi2_val = float(sum(
            (o - e) ** 2 / e for o, e in zip(obs_merged, exp_merged) if e > 0
        ))
        df = len(obs_merged) - 3  # estimated 2 parameters (n, p) + 1
        df = max(1, df)
        p_val = float(1 - sp_stats.chi2.cdf(chi2_val, df))
    else:
        chi2_val = 0.0
        p_val = 1.0
        df = 0

    return {
        "mean": round(mean_len, 3),
        "std": round(std_len, 3),
        "max_length": max_len,
        "n_words": total,
        "binomial_n": round(n_est, 2),
        "binomial_p": round(p_est, 4),
        "chi2_binomial": round(chi2_val, 2),
        "chi2_df": df,
        "chi2_p": round(p_val, 6),
        "distribution": {str(k): len_freq.get(k, 0) for k in range(1, max_len + 1)},
    }


def _merge_small_bins(observed: list, expected: list,
                      min_expected: float = 5) -> tuple[list, list]:
    """Merge tail bins where expected < min_expected."""
    obs_m, exp_m = [], []
    obs_acc, exp_acc = 0, 0.0

    for o, e in zip(observed, expected):
        obs_acc += o
        exp_acc += e
        if exp_acc >= min_expected:
            obs_m.append(obs_acc)
            exp_m.append(exp_acc)
            obs_acc, exp_acc = 0, 0.0

    # Add remainder to last bin
    if obs_acc > 0 or exp_acc > 0:
        if obs_m:
            obs_m[-1] += obs_acc
            exp_m[-1] += exp_acc
        else:
            obs_m.append(obs_acc)
            exp_m.append(exp_acc)

    return obs_m, exp_m


