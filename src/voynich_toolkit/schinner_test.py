"""
Phase 9 — Schinner (2007) quasi-random test.

Replicates statistical tests from Schinner (2007, Cryptologia) and
Timm & Schinner (2014) that argued the Voynich text was generated by
a stochastic process rather than encoding natural language.

Six subtests:

  9a — Character entropy chain
       H1 (character entropy), h2 (conditional bigram entropy),
       h3 (conditional trigram entropy). Compare to natural language
       reference values. Schinner's key evidence: h2 is anomalously low.

  9b — Word-length distribution
       Fit word lengths to a binomial distribution. Schinner/Timm showed
       Voynich word lengths follow an almost perfect binomial, unlike
       natural languages (which are skewed/log-normal).

  9c — Zipf's laws (both)
       First law: rank-frequency power law.
       Second law: frequency spectrum (how many types have frequency f).

  9d — Long-range correlations (Detrended Fluctuation Analysis)
       Map characters to numerical values, compute DFA exponent alpha.
       alpha ~ 0.5 = uncorrelated, alpha > 0.5 = long-range correlations.

  9e — Word repeat distance distribution
       Measure distances between consecutive occurrences of each word type.
       Quasi-random text: geometric distribution. Natural language: bursty
       (clustered, deviating from geometric).

  9f — Structural falsification
       Generate quasi-random text via order-2 Markov chain and test whether
       it reproduces our confirmed structural properties (Phases 7-8).
       Expected: Markov reproduces character-level stats but FAILS on
       line boundaries, paragraph markers, and section-specific vocabulary.

Output:
  schinner_test.json
  schinner_test_summary.txt
  DB table: schinner_test
"""

