def save_to_db(results: dict, db_path: Path) -> None:
    """Save all results to schinner_test table."""
    conn = sqlite3.connect(str(db_path))
    cur = conn.cursor()

    cur.execute("DROP TABLE IF EXISTS schinner_test")
    cur.execute("""
        CREATE TABLE schinner_test (
            test        TEXT,
            key         TEXT,
            observed    REAL,
            reference   REAL,
            null_mean   REAL,
            null_std    REAL,
            z_score     REAL,
            detail_json TEXT,
            PRIMARY KEY (test, key)
        )
    """)

    def ins(test, key, observed=None, reference=None,
            null_mean=None, null_std=None, z=None, detail=None):
        cur.execute(
            "INSERT INTO schinner_test VALUES (?,?,?,?,?,?,?,?)",
            (test, key, observed, reference, null_mean, null_std,
             round(z, 3) if z is not None else None,
             json.dumps(detail) if detail else None),
        )

    # 9a entropy
    ent = results["9a_entropy"]
    null_ent = results.get("9a_entropy_null", {})
    ins("9a_entropy", "H1", ent["H1"], REF_LATIN["H1"])
    ins("9a_entropy", "h2_conditional", ent["h2_conditional"], REF_LATIN["h2"],
        null_ent.get("null_mean_h2"), null_ent.get("null_std_h2"),
        results.get("9a_z_h2"))
    ins("9a_entropy", "H2_total", ent["H2_total"])
    ins("9a_entropy", "h3_conditional", ent["h3_conditional"])
    ins("9a_entropy", "reduction_ratio", ent["reduction_ratio"],
        detail={"interpretation": "fraction of uncertainty removed by context"})

    # 9b word length
    wl = results["9b_wordlen"]
    ins("9b_wordlen", "mean", wl["mean"])
    ins("9b_wordlen", "std", wl["std"])
    ins("9b_wordlen", "binomial_n", wl["binomial_n"])
    ins("9b_wordlen", "binomial_p", wl["binomial_p"])
    ins("9b_wordlen", "chi2_binomial", wl["chi2_binomial"],
        detail={"df": wl["chi2_df"], "p": wl["chi2_p"]})

    # 9c Zipf
    zf = results["9c_zipf"]
    ins("9c_zipf", "first_law_slope", zf["first_law_slope"], reference=-1.0)
    ins("9c_zipf", "first_law_r2", zf["first_law_r2"])
    ins("9c_zipf", "second_law_slope", zf["second_law_slope"], reference=-0.5)
    ins("9c_zipf", "second_law_r2", zf["second_law_r2"])

    # 9d DFA
    dfa = results["9d_dfa"]
    null_dfa_r = results.get("9d_dfa_null", {})
    if dfa.get("alpha") is not None:
        ins("9d_dfa", "alpha", dfa["alpha"], reference=0.5,
            null_mean=null_dfa_r.get("null_mean_alpha"),
            null_std=null_dfa_r.get("null_std_alpha"),
            z=results.get("9d_z_alpha"),
            detail={"interpretation": dfa.get("interpretation"),
                    "r2": dfa.get("alpha_r2")})

    # 9e repeat distances
    rd = results["9e_repeat"]
    null_rd = results.get("9e_repeat_null", {})
    ins("9e_repeat", "mean_ks", rd["mean_ks_statistic"],
        null_mean=null_rd.get("null_mean_ks"),
        null_std=null_rd.get("null_std_ks"),
        z=results.get("9e_z_ks"))
    ins("9e_repeat", "pct_geometric", rd["pct_geometric"])
    ins("9e_repeat", "overall_cv", rd["overall_cv"])

    # 9f structural
    sf = results["9f_structural"]
    for prop, data in sf["properties"].items():
        ins("9f_structural", prop,
            data.get("real"), null_mean=data.get("null_mean"),
            null_std=data.get("null_std"), z=data.get("z_score"))

    conn.commit()
    conn.close()


