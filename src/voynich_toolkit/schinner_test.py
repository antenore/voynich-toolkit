def compute_repeat_distances(pages: list[dict], min_freq: int = 5) -> dict:
    """Measure distances between consecutive occurrences of each word type.

    For quasi-random text, distances should follow a geometric distribution
    (memoryless). Natural language shows burstiness (clustered occurrences).
    """
    all_words = _extract_all_words(pages)
    freq = Counter(all_words)

    # Only words with enough occurrences for meaningful stats
    target_words = {w for w, c in freq.items() if c >= min_freq}

    # Track positions
    positions: dict[str, list[int]] = defaultdict(list)
    for i, w in enumerate(all_words):
        if w in target_words:
            positions[w].append(i)

    # Compute distances and KS-test against geometric for each word
    ks_stats = []
    all_distances = []

    for word, pos_list in positions.items():
        if len(pos_list) < 2:
            continue

        distances = [pos_list[i + 1] - pos_list[i]
                     for i in range(len(pos_list) - 1)]
        all_distances.extend(distances)

        if len(distances) < 5:
            continue

        # Fit geometric: p = 1 / mean_distance
        mean_d = float(np.mean(distances))
        if mean_d <= 0:
            continue
        p_geom = 1.0 / mean_d

        # KS test against geometric
        ks_stat, ks_p = sp_stats.kstest(
            distances,
            lambda x: sp_stats.geom.cdf(x, p_geom)
        )
        ks_stats.append({
            "word": word,
            "n_occurrences": len(pos_list),
            "mean_distance": round(mean_d, 1),
            "ks_statistic": round(float(ks_stat), 4),
            "ks_p": round(float(ks_p), 6),
        })

    # Overall: what fraction of word types fit geometric?
    n_tested = len(ks_stats)
    n_geometric = sum(1 for ks in ks_stats if ks["ks_p"] > 0.05)
    mean_ks = float(np.mean([ks["ks_statistic"] for ks in ks_stats])) if ks_stats else 0.0

    # Overall distance distribution stats
    if all_distances:
        overall_mean = float(np.mean(all_distances))
        overall_cv = float(np.std(all_distances) / overall_mean) if overall_mean > 0 else 0.0
    else:
        overall_mean, overall_cv = 0.0, 0.0

    return {
        "n_word_types_tested": n_tested,
        "n_geometric_fit": n_geometric,
        "pct_geometric": round(100 * n_geometric / n_tested, 1) if n_tested > 0 else 0.0,
        "mean_ks_statistic": round(mean_ks, 4),
        "overall_mean_distance": round(overall_mean, 1),
        "overall_cv": round(overall_cv, 3),
        "top_bursty": sorted(ks_stats, key=lambda x: -x["ks_statistic"])[:10],
        "top_geometric": sorted(ks_stats, key=lambda x: x["ks_statistic"])[:10],
    }


def null_repeat_distances(pages: list[dict], min_freq: int = 5,
                          n_perms: int = 100, seed: int = SEED) -> dict:
    """Null: shuffle word order, recompute mean KS statistic."""
    rng = random.Random(seed)
    all_words = _extract_all_words(pages)
    freq = Counter(all_words)
    target_words = {w for w, c in freq.items() if c >= min_freq}

    nulls = []
    for _ in range(n_perms):
        shuffled = list(all_words)
        rng.shuffle(shuffled)

        positions: dict[str, list[int]] = defaultdict(list)
        for i, w in enumerate(shuffled):
            if w in target_words:
                positions[w].append(i)

        ks_vals = []
        for word, pos_list in positions.items():
            if len(pos_list) < 2:
                continue
            distances = [pos_list[i + 1] - pos_list[i]
                         for i in range(len(pos_list) - 1)]
            if len(distances) < 5:
                continue
            mean_d = float(np.mean(distances))
            if mean_d <= 0:
                continue
            p_geom = 1.0 / mean_d
            ks_stat, _ = sp_stats.kstest(
                distances, lambda x, p=p_geom: sp_stats.geom.cdf(x, p)
            )
            ks_vals.append(float(ks_stat))

        if ks_vals:
            nulls.append(float(np.mean(ks_vals)))

    if not nulls:
        return {"null_mean_ks": None, "null_std_ks": None}

    return {
        "null_mean_ks": round(float(np.mean(nulls)), 4),
        "null_std_ks": round(float(np.std(nulls, ddof=1)), 4),
    }


