def _z_score(observed: float, null_mean: float, null_std: float) -> float | None:
    if null_std < 1e-10:
        return None
    return (observed - null_mean) / null_std


def _extract_char_stream(pages: list[dict]) -> list[str]:
    """Flatten all EVA characters from the corpus (only valid EVA chars)."""
    chars = []
    for page in pages:
        for word in page["words"]:
            for ch in word:
                if ch in EVA_CHARS:
                    chars.append(ch)
    return chars


def _extract_all_words(pages: list[dict]) -> list[str]:
    """Flat list of all words."""
    return [w for p in pages for w in p["words"]]


