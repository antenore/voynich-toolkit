def run(config: ToolkitConfig, force: bool = False, **kwargs):
    """Phase 9: Schinner quasi-random test."""
    report_path = config.stats_dir / "schinner_test.json"
    summary_path = config.stats_dir / "schinner_test_summary.txt"

    if report_path.exists() and not force:
        click.echo("  Schinner test report exists. Use --force to re-run.")
        return

    config.ensure_dirs()
    print_header("PHASE 9 — Schinner (2007) Quasi-Random Test")

    # 1. Parse EVA corpus
    print_step("Parsing EVA corpus...")
    eva_file = config.eva_data_dir / "LSI_ivtff_0d.txt"
    if not eva_file.exists():
        raise click.ClickException(f"EVA file not found: {eva_file}")
    eva_data = parse_eva_words(eva_file)
    pages = eva_data["pages"]
    click.echo(f"    {eva_data['total_words']:,} words, "
               f"{eva_data['total_chars']:,} chars, {len(pages)} pages")

    results = {}

    # 2. 9a — Character entropy chain
    print_step("9a: Character entropy chain...")
    ent = compute_entropy_chain(pages)
    results["9a_entropy"] = ent
    click.echo(f"    H1={ent['H1']:.4f}, h2={ent['h2_conditional']:.4f}, "
               f"h3={ent['h3_conditional']:.4f}")

    print_step("9a: Null model (shuffled chars within words)...")
    null_ent = null_entropy_chain(pages)
    results["9a_entropy_null"] = null_ent
    z_h2 = _z_score(ent["h2_conditional"], null_ent["null_mean_h2"],
                     null_ent["null_std_h2"])
    results["9a_z_h2"] = round(z_h2, 2) if z_h2 is not None else None
    click.echo(f"    h2 null: {null_ent['null_mean_h2']:.4f} +/- "
               f"{null_ent['null_std_h2']:.4f}, z={z_h2:+.2f}" if z_h2 else "")

    # 3. 9b — Word-length distribution
    print_step("9b: Word-length distribution...")
    wl = compute_wordlen_distribution(pages)
    results["9b_wordlen"] = wl
    click.echo(f"    mean={wl['mean']:.2f}, std={wl['std']:.2f}, "
               f"B(n={wl['binomial_n']:.1f}, p={wl['binomial_p']:.3f}), "
               f"chi2 p={wl['chi2_p']:.4f}")

    # 4. 9c — Zipf's laws
    print_step("9c: Zipf's laws...")
    zf = compute_zipf_laws(pages)
    results["9c_zipf"] = zf
    click.echo(f"    1st law slope={zf['first_law_slope']:.3f} (R²={zf['first_law_r2']:.3f}), "
               f"2nd law slope={zf['second_law_slope']:.3f}")

    # 5. 9d — DFA
    print_step("9d: Detrended Fluctuation Analysis...")
    dfa_result = compute_dfa(pages)
    results["9d_dfa"] = dfa_result
    if dfa_result.get("alpha") is not None:
        click.echo(f"    alpha={dfa_result['alpha']:.4f} "
                   f"({dfa_result['interpretation']})")

        print_step("9d: DFA null model (shuffled characters)...")
        null_dfa_result = null_dfa(pages)
        results["9d_dfa_null"] = null_dfa_result
        z_alpha = _z_score(dfa_result["alpha"],
                           null_dfa_result["null_mean_alpha"],
                           null_dfa_result["null_std_alpha"])
        results["9d_z_alpha"] = round(z_alpha, 2) if z_alpha is not None else None
        if z_alpha is not None:
            click.echo(f"    null alpha: {null_dfa_result['null_mean_alpha']:.4f}, "
                       f"z={z_alpha:+.2f}")
    else:
        click.echo(f"    Skipped: {dfa_result.get('reason')}")

    # 6. 9e — Repeat distances
    print_step("9e: Word repeat distance distribution...")
    rd = compute_repeat_distances(pages)
    results["9e_repeat"] = rd
    click.echo(f"    {rd['n_word_types_tested']} types, "
               f"{rd['pct_geometric']:.1f}% geometric fit, "
               f"mean KS={rd['mean_ks_statistic']:.4f}")

    print_step("9e: Null model (shuffled word order)...")
    null_rd = null_repeat_distances(pages)
    results["9e_repeat_null"] = null_rd
    z_ks = _z_score(rd["mean_ks_statistic"],
                     null_rd.get("null_mean_ks"),
                     null_rd.get("null_std_ks"))
    results["9e_z_ks"] = round(z_ks, 2) if z_ks is not None else None
    if z_ks is not None:
        click.echo(f"    null KS: {null_rd['null_mean_ks']:.4f}, z={z_ks:+.2f}")

    # 7. 9f — Structural falsification
    print_step("9f: Structural falsification (Markov chain, 50 iterations)...")
    sf = structural_falsification(pages)
    results["9f_structural"] = sf
    click.echo(f"    {sf['n_reproduced']}/{sf['n_total']} properties reproduced")
    click.echo(f"    Verdict: {sf['verdict']}")

    # 8. Save JSON
    print_step("Saving JSON report...")
    with open(report_path, "w", encoding="utf-8") as f:
        json.dump(results, f, ensure_ascii=False, indent=2, default=str)
    click.echo(f"    {report_path}")

    # 9. Save TXT summary
    summary = format_summary(results)
    with open(summary_path, "w", encoding="utf-8") as f:
        f.write(summary)
    click.echo(f"    {summary_path}")

    # 10. Save to DB
    print_step("Writing DB table...")
    db_path = config.output_dir.parent / "voynich.db"
    if db_path.exists():
        save_to_db(results, db_path)
        click.echo(f"    {db_path}")
    else:
        click.echo(f"    WARN: DB not found at {db_path} — skipping")

    # 11. Print summary
    click.echo(f"\n{summary}")

    print_header("Phase 9 complete")
