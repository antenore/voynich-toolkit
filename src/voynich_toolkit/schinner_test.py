def build_markov_chain(pages: list[dict], order: int = 2) -> dict:
    """Build a character-level Markov chain of given order from the corpus.

    Returns transition table: context (tuple of chars) -> {next_char: count}.
    Also returns word boundary statistics for generating realistic word lengths.
    """
    all_words = _extract_all_words(pages)

    # Character-level Markov with word boundary markers
    BOUNDARY = "|"
    transitions: dict[tuple, Counter] = defaultdict(Counter)

    for word in all_words:
        # Prepend boundary markers for context
        seq = [BOUNDARY] * order + list(word) + [BOUNDARY]
        for i in range(len(seq) - order):
            context = tuple(seq[i:i + order])
            next_ch = seq[i + order]
            transitions[context][next_ch] += 1

    return {"transitions": transitions, "order": order, "boundary": BOUNDARY}


def generate_markov_text(model: dict, n_words: int,
                         seed: int = SEED) -> list[str]:
    """Generate words from a Markov model."""
    rng = random.Random(seed)
    transitions = model["transitions"]
    order = model["order"]
    boundary = model["boundary"]

    words = []
    for _ in range(n_words):
        context = tuple([boundary] * order)
        chars = []
        for _ in range(30):  # max word length safety
            options = transitions.get(context)
            if not options:
                break
            # Weighted random choice
            total = sum(options.values())
            r = rng.random() * total
            cumul = 0
            chosen = boundary
            for ch, count in options.items():
                cumul += count
                if cumul >= r:
                    chosen = ch
                    break
            if chosen == boundary:
                break
            chars.append(chosen)
            context = tuple(list(context[1:]) + [chosen])

        if chars:
            words.append("".join(chars))

    return words


def structural_falsification(pages: list[dict], n_perms: int = 50,
                             seed: int = SEED) -> dict:
    """Generate quasi-random corpora via Markov chain and test
    whether they reproduce confirmed structural properties.

    Tests:
      1. Line-end 'm' concentration (real: 71% line-final, z=+55.5)
      2. Simple gallows at paragraph start (real: z=+10.73)
      3. Line self-containment (real: z=-3.60, very few cross-boundary repeats)
      4. Section vocabulary specificity (real: MI z=+40.24)
    """
    rng = random.Random(seed)

    # Real corpus measurements
    real_measures = _measure_structural_properties(pages)

    # Build Markov model from real corpus
    model = build_markov_chain(pages, order=2)

    # Count words and lines per page in real corpus for structure template
    page_templates = []
    for page in pages:
        n_lines = len(page.get("line_words", []))
        words_per_line = [len(line) for line in page.get("line_words", [])]
        page_templates.append({
            "section": page["section"],
            "n_lines": n_lines,
            "words_per_line": words_per_line,
        })

    # Generate synthetic corpora with same structure
    null_measures = {k: [] for k in real_measures}

    for perm_i in range(n_perms):
        # Generate enough words
        total_words_needed = sum(
            sum(t["words_per_line"]) for t in page_templates
        )
        synthetic_words = generate_markov_text(
            model, total_words_needed + 100, seed=seed + perm_i
        )

        # Fill into page/line structure
        synthetic_pages = []
        word_idx = 0
        for template in page_templates:
            page_words = []
            page_line_words = []
            for wpl in template["words_per_line"]:
                line = synthetic_words[word_idx:word_idx + wpl]
                word_idx += wpl
                page_line_words.append(line)
                page_words.extend(line)
                if word_idx >= len(synthetic_words):
                    break

            synthetic_pages.append({
                "section": template["section"],
                "words": page_words,
                "line_words": page_line_words,
            })

        synth_measures = _measure_structural_properties(synthetic_pages)
        for k, v in synth_measures.items():
            null_measures[k].append(v)

    # Compare real vs synthetic
    results = {}
    for prop, real_val in real_measures.items():
        nulls = [v for v in null_measures[prop] if v is not None]
        if not nulls:
            results[prop] = {
                "real": real_val, "reproduced": False,
                "reason": "no valid null samples",
            }
            continue

        null_mean = float(np.mean(nulls))
        null_std = float(np.std(nulls, ddof=1))
        z = _z_score(real_val, null_mean, null_std)

        reproduced = z is not None and abs(z) < 2.0
        results[prop] = {
            "real": round(real_val, 4) if real_val is not None else None,
            "null_mean": round(null_mean, 4),
            "null_std": round(null_std, 4),
            "z_score": round(z, 2) if z is not None else None,
            "reproduced": reproduced,
        }

    n_repro = sum(1 for r in results.values() if r.get("reproduced"))
    n_total = len(results)

    return {
        "properties": results,
        "n_reproduced": n_repro,
        "n_total": n_total,
        "verdict": "QUASI-RANDOM SUFFICIENT" if n_repro == n_total
                   else "QUASI-RANDOM INSUFFICIENT",
    }


def _measure_structural_properties(pages: list[dict]) -> dict:
    """Measure key structural properties on a corpus (real or synthetic).

    Returns dict of property_name -> float value.
    """
    results = {}

    # 1. Line-end 'm' fraction
    m_total = 0
    m_line_final = 0
    for page in pages:
        for line in page.get("line_words", []):
            if not line:
                continue
            for word in line:
                m_total += word.count("m")
            last_word = line[-1]
            if last_word.endswith("m"):
                m_line_final += 1

    results["m_line_final_rate"] = m_line_final / max(1, m_total)

    # 2. Simple gallows at line[0] rate
    simple_gallows = {"t", "k", "p", "f"}
    gallows_at_start = 0
    gallows_total = 0
    for page in pages:
        for i, line in enumerate(page.get("line_words", [])):
            if not line:
                continue
            first_word = line[0]
            for ch in first_word:
                if ch in simple_gallows:
                    gallows_at_start += 1
            for word in line:
                for ch in word:
                    if ch in simple_gallows:
                        gallows_total += 1

    results["gallows_first_word_rate"] = (
        gallows_at_start / max(1, gallows_total)
    )

    # 3. Cross-boundary repeat rate (lower = more self-contained lines)
    n_boundaries = 0
    n_repeats = 0
    for page in pages:
        lines = page.get("line_words", [])
        for i in range(len(lines) - 1):
            if not lines[i] or not lines[i + 1]:
                continue
            n_boundaries += 1
            if lines[i][-1] == lines[i + 1][0]:
                n_repeats += 1

    results["cross_boundary_rate"] = n_repeats / max(1, n_boundaries)

    # 4. Section vocabulary specificity (simplified MI proxy)
    # Compute type overlap between sections (lower = more specific)
    section_types: dict[str, set] = defaultdict(set)
    for page in pages:
        sec = page.get("section", "?")
        section_types[sec].update(page["words"])

    sections = [s for s in section_types if len(section_types[s]) >= 10]
    if len(sections) >= 2:
        overlaps = []
        for i in range(len(sections)):
            for j in range(i + 1, len(sections)):
                a = section_types[sections[i]]
                b = section_types[sections[j]]
                jaccard = len(a & b) / len(a | b) if (a | b) else 0
                overlaps.append(jaccard)
        results["section_jaccard_mean"] = float(np.mean(overlaps))
    else:
        results["section_jaccard_mean"] = None

    return results


