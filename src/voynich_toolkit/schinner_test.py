# 9a — Character entropy chain
# =====================================================================

def compute_entropy_chain(pages: list[dict]) -> dict:
    """Compute character-level entropy: H1, H2 (bigram), h2 (conditional),
    H3 (trigram), h3 (conditional).

    H1 = -sum(p(c) * log2(p(c)))
    H2 = -sum(p(c1,c2) * log2(p(c1,c2))) / 1  [total bigram entropy]
    h2 = H2_total - H1 = conditional entropy given previous char
    """
    chars = _extract_char_stream(pages)
    n = len(chars)

    # H1: single-character entropy
    char_freq = Counter(chars)
    h1 = -sum((c / n) * math.log2(c / n) for c in char_freq.values() if c > 0)

    # Bigram entropy (H2_total = joint entropy of pairs)
    bigram_freq: Counter[str] = Counter()
    for i in range(n - 1):
        bigram_freq[chars[i] + chars[i + 1]] += 1
    n_bigrams = sum(bigram_freq.values())
    h2_total = -sum(
        (c / n_bigrams) * math.log2(c / n_bigrams)
        for c in bigram_freq.values() if c > 0
    )
    # Conditional: h2 = H2_total - H1
    h2_cond = h2_total - h1

    # Trigram entropy
    trigram_freq: Counter[str] = Counter()
    for i in range(n - 2):
        trigram_freq[chars[i] + chars[i + 1] + chars[i + 2]] += 1
    n_trigrams = sum(trigram_freq.values())
    h3_total = -sum(
        (c / n_trigrams) * math.log2(c / n_trigrams)
        for c in trigram_freq.values() if c > 0
    )
    h3_cond = h3_total - h2_total

    # Entropy reduction ratio: how much knowing previous char reduces uncertainty
    reduction_ratio = 1 - (h2_cond / h1) if h1 > 0 else 0.0

    return {
        "H1": round(h1, 4),
        "h2_conditional": round(h2_cond, 4),
        "H2_total": round(h2_total, 4),
        "h3_conditional": round(h3_cond, 4),
        "H3_total": round(h3_total, 4),
        "reduction_ratio": round(reduction_ratio, 4),
        "n_chars": n,
        "n_bigrams": n_bigrams,
        "n_trigrams": n_trigrams,
        "alphabet_size": len(char_freq),
        "references": REFERENCES,
    }


def null_entropy_chain(pages: list[dict], n_perms: int = N_PERM,
                       seed: int = SEED) -> dict:
    """Null model: shuffle characters within each word, recompute h2.

    This preserves word lengths and character frequencies but destroys
    sequential character structure.
    """
    rng = random.Random(seed)
    all_words = _extract_all_words(pages)

    nulls_h2 = []
    for _ in range(n_perms):
        shuffled_words = []
        for w in all_words:
            chars = list(w)
            rng.shuffle(chars)
            shuffled_words.append("".join(chars))

        # Compute h2 on shuffled stream
        stream = []
        for w in shuffled_words:
            for ch in w:
                if ch in EVA_CHARS:
                    stream.append(ch)

        n = len(stream)
        cf = Counter(stream)
        h1 = -sum((c / n) * math.log2(c / n) for c in cf.values() if c > 0)

        bf: Counter[str] = Counter()
        for i in range(n - 1):
            bf[stream[i] + stream[i + 1]] += 1
        nb = sum(bf.values())
        h2t = -sum((c / nb) * math.log2(c / nb) for c in bf.values() if c > 0)
        nulls_h2.append(h2t - h1)

    return {
        "null_mean_h2": float(np.mean(nulls_h2)),
        "null_std_h2": float(np.std(nulls_h2, ddof=1)),
    }


