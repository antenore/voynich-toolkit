def save_to_db(results: dict, db_path: Path) -> None:
    conn = sqlite3.connect(str(db_path))
    cur = conn.cursor()

    cur.execute("DROP TABLE IF EXISTS timm_test")
    cur.execute("""
        CREATE TABLE timm_test (
            test        TEXT,
            key         TEXT,
            observed    REAL,
            null_mean   REAL,
            null_std    REAL,
            z_score     REAL,
            detail_json TEXT,
            PRIMARY KEY (test, key)
        )
    """)

    def _insert(test: str, key: str, observed, null_mean=None,
                null_std=None, z_score=None, detail=None):
        cur.execute("INSERT INTO timm_test VALUES (?,?,?,?,?,?,?)",
                    (test, key, observed, null_mean, null_std, z_score,
                     json.dumps(detail) if detail else None))

    # 10a
    prox = results["proximity_clustering"]
    _insert("10a_proximity", "exact_match_rate",
            prox["observed"]["exact_match_proximity_rate"],
            prox.get("null_mean"), prox.get("null_std"),
            prox.get("z_score"), prox["observed"])

    # 10b
    decay = results["similarity_decay"]
    _insert("10b_decay", "correlation",
            decay.get("correlation_distance_similarity"),
            detail=decay)

    # 10c
    plocal = results["page_local_vocab"]
    _insert("10c_page_local", "ratio",
            plocal["observed"].get("ratio"),
            plocal.get("null_mean"), plocal.get("null_std"),
            plocal.get("z_score"), plocal["observed"])

    # 10d
    fw = results["first_word_repetition"]
    _insert("10d_first_word", "repetition_rate",
            fw["observed"]["first_word_repetition_rate"],
            fw.get("null_mean"), fw.get("null_std"),
            fw.get("z_score"), fw["observed"])

    # 10e
    struct = results["structural"]
    for prop, d in struct["properties"].items():
        _insert("10e_structural", prop,
                d.get("real"), d.get("null_mean"), d.get("null_std"),
                d.get("z_score"), d)

    conn.commit()
    conn.close()


