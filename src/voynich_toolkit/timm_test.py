def test_similarity_decay(lines: list[dict], max_distance: int = 20) -> dict:
    """Measure vocabulary similarity (Jaccard) between lines at distance d.

    If self-citation: similarity decays with distance.
    """
    # Build line vocabularies
    line_vocabs = [set(line["words"]) for line in lines]
    n_lines = len(line_vocabs)

    # Compute mean Jaccard at each distance
    distance_sims: dict[int, list[float]] = defaultdict(list)

    for i in range(n_lines):
        if not line_vocabs[i]:
            continue
        for d in range(1, min(max_distance + 1, n_lines - i)):
            j = i + d
            if not line_vocabs[j]:
                continue
            sim = _jaccard(line_vocabs[i], line_vocabs[j])
            distance_sims[d].append(sim)

    mean_by_distance = {}
    for d in range(1, max_distance + 1):
        sims = distance_sims.get(d, [])
        if sims:
            mean_by_distance[d] = round(float(np.mean(sims)), 6)

    # Compute correlation: distance vs similarity
    distances = []
    similarities = []
    for d, mean_sim in sorted(mean_by_distance.items()):
        distances.append(d)
        similarities.append(mean_sim)

    if len(distances) > 2:
        corr = float(np.corrcoef(distances, similarities)[0, 1])
    else:
        corr = None

    return {
        "mean_similarity_by_distance": mean_by_distance,
        "correlation_distance_similarity": round(corr, 4) if corr is not None else None,
        "sim_at_d1": mean_by_distance.get(1),
        "sim_at_d5": mean_by_distance.get(5),
        "sim_at_d20": mean_by_distance.get(20),
    }


