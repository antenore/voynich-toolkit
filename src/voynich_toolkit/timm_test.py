def test_page_local_vocab(pages: list[dict]) -> dict:
    """Compare vocabulary overlap between adjacent pages vs random pairs."""
    page_vocabs = [set(p["words"]) for p in pages if len(p["words"]) >= 10]
    n_pages = len(page_vocabs)

    if n_pages < 4:
        return {"skipped": True, "reason": "too few pages"}

    # Adjacent pairs
    adj_jaccards = []
    for i in range(n_pages - 1):
        j = _jaccard(page_vocabs[i], page_vocabs[i + 1])
        adj_jaccards.append(j)

    # Random pairs (same number as adjacent, for fair comparison)
    rng = random.Random(SEED)
    random_jaccards = []
    for _ in range(len(adj_jaccards)):
        i, j = rng.sample(range(n_pages), 2)
        random_jaccards.append(_jaccard(page_vocabs[i], page_vocabs[j]))

    adj_mean = float(np.mean(adj_jaccards))
    rand_mean = float(np.mean(random_jaccards))

    return {
        "adjacent_jaccard_mean": round(adj_mean, 4),
        "random_jaccard_mean": round(rand_mean, 4),
        "ratio": round(adj_mean / rand_mean, 4) if rand_mean > 0 else None,
        "n_adjacent_pairs": len(adj_jaccards),
    }


def null_page_local_vocab(pages: list[dict], n_perms: int = N_NULL,
                           seed: int = SEED) -> dict:
    """Null: shuffle page order within section, recompute adjacent Jaccard."""
    rng = random.Random(seed)

    # Group pages by section
    section_pages: dict[str, list[int]] = defaultdict(list)
    page_vocabs = []
    for i, p in enumerate(pages):
        if len(p["words"]) < 10:
            continue
        section_pages[p.get("section", "?")].append(len(page_vocabs))
        page_vocabs.append(set(p["words"]))

    n_pages = len(page_vocabs)
    if n_pages < 4:
        return {"null_mean": 0.0, "null_std": 0.0}

    null_ratios: list[float] = []

    for _ in range(n_perms):
        # Shuffle within section
        shuffled_order = list(range(n_pages))
        for sec, indices in section_pages.items():
            shuffled_indices = indices[:]
            rng.shuffle(shuffled_indices)
            for orig, shuf in zip(indices, shuffled_indices):
                shuffled_order[orig] = shuf

        # Adjacent pairs in shuffled order
        adj = []
        for i in range(n_pages - 1):
            adj.append(_jaccard(page_vocabs[shuffled_order[i]],
                                page_vocabs[shuffled_order[i + 1]]))

        # Random pairs
        rand = []
        for _ in range(len(adj)):
            a, b = rng.sample(range(n_pages), 2)
            rand.append(_jaccard(page_vocabs[a], page_vocabs[b]))

        adj_mean = float(np.mean(adj))
        rand_mean = float(np.mean(rand))
        ratio = adj_mean / rand_mean if rand_mean > 0 else 1.0
        null_ratios.append(ratio)

    return {
        "null_mean": round(float(np.mean(null_ratios)), 4),
        "null_std": round(float(np.std(null_ratios, ddof=1)), 6),
    }


