def run(config: ToolkitConfig, force: bool = False, **kwargs) -> None:
    """Phase 10: Timm (2014) self-citation test."""
    report_path = config.stats_dir / "timm_test.json"
    summary_path = config.stats_dir / "timm_test_summary.txt"

    if report_path.exists() and not force:
        click.echo("  timm_test report exists. Use --force to re-run.")
        return

    config.ensure_dirs()
    print_header("PHASE 10 — Timm (2014) Self-Citation Test")

    # 1. Parse corpus
    print_step("Parsing EVA corpus...")
    eva_file = config.eva_data_dir / "LSI_ivtff_0d.txt"
    if not eva_file.exists():
        raise click.ClickException(f"EVA file not found: {eva_file}")
    eva_data = parse_eva_words(eva_file)
    pages = eva_data["pages"]
    click.echo(f"    {eva_data['total_words']:,} words, {len(pages)} pages")

    # Also parse IVTFF lines for line-level tests
    print_step("Parsing IVTFF lines...")
    all_lines = parse_ivtff_lines(eva_file)
    content_lines = [l for l in all_lines
                     if l.get("para_type") != "label" and l.get("words")]

    # Add section info to lines (from pages)
    folio_section = {}
    for p in pages:
        folio_section[p["folio"]] = p.get("section", "?")
    for line in content_lines:
        line["section"] = folio_section.get(line["folio"], "?")

    click.echo(f"    {len(content_lines)} content lines")

    # 2. Test 10a: Proximity clustering
    print_step("10a: Proximity clustering...")
    prox_obs = test_proximity_clustering(content_lines)
    click.echo(f"    Exact-match: {prox_obs['exact_match_proximity_rate']:.1%} "
               f"({prox_obs['exact_match_n_pairs']} pairs)")
    click.echo(f"    Edit-dist:   {prox_obs['edit_distance_proximity_rate']:.1%} "
               f"({prox_obs['edit_distance_n_pairs']} pairs)")

    print_step("10a: Null model (500 permutations)...")
    prox_null = null_proximity_clustering(content_lines, n_perms=N_NULL, seed=SEED)
    z_prox = _z_score(prox_obs["exact_match_proximity_rate"],
                       prox_null["null_mean"], prox_null["null_std"])
    click.echo(f"    Null: mean={prox_null['null_mean']:.1%}, "
               f"z={z_prox:+.2f}" if z_prox is not None else "    z=n/a")

    proximity_results = {
        "observed": prox_obs,
        "null_mean": prox_null["null_mean"],
        "null_std": prox_null["null_std"],
        "z_score": round(z_prox, 2) if z_prox is not None else None,
    }

    # 3. Test 10b: Similarity decay
    print_step("10b: Vocabulary similarity decay...")
    decay_results = test_similarity_decay(content_lines)
    corr = decay_results.get("correlation_distance_similarity")
    click.echo(f"    Correlation: {corr:+.4f}" if corr else "    n/a")

    # 4. Test 10c: Page-local vocabulary
    print_step("10c: Page-local vocabulary...")
    plocal_obs = test_page_local_vocab(pages)
    if not plocal_obs.get("skipped"):
        click.echo(f"    Adjacent: {plocal_obs['adjacent_jaccard_mean']:.4f}, "
                   f"Random: {plocal_obs['random_jaccard_mean']:.4f}")

        print_step("10c: Null model (500 permutations)...")
        plocal_null = null_page_local_vocab(pages, n_perms=N_NULL, seed=SEED)
        z_page = _z_score(plocal_obs.get("ratio", 1.0),
                           plocal_null["null_mean"], plocal_null["null_std"])
        click.echo(f"    z={z_page:+.2f}" if z_page is not None else "    z=n/a")
    else:
        plocal_null = {"null_mean": 0, "null_std": 0}
        z_page = None

    page_local_results = {
        "observed": plocal_obs,
        "null_mean": plocal_null["null_mean"],
        "null_std": plocal_null["null_std"],
        "z_score": round(z_page, 2) if z_page is not None else None,
    }

    # 5. Test 10d: First-word repetition
    print_step("10d: First-word repetition...")
    fw_obs = test_first_word_repetition(content_lines)
    click.echo(f"    Rate: {fw_obs['first_word_repetition_rate']:.1%}")

    print_step("10d: Null model (500 permutations)...")
    fw_null = null_first_word_repetition(content_lines, n_perms=N_NULL, seed=SEED)
    z_fw = _z_score(fw_obs["first_word_repetition_rate"],
                     fw_null["null_mean"], fw_null["null_std"])
    click.echo(f"    Null: mean={fw_null['null_mean']:.1%}, "
               f"z={z_fw:+.2f}" if z_fw is not None else "    z=n/a")

    fw_results = {
        "observed": fw_obs,
        "null_mean": fw_null["null_mean"],
        "null_std": fw_null["null_std"],
        "z_score": round(z_fw, 2) if z_fw is not None else None,
    }

    # 6. Test 10e: Self-citation generator
    print_step("10e: Self-citation generator (50 synthetic corpora)...")
    structural_results = test_self_citation_structural(pages, n_synth=N_SYNTH, seed=SEED)
    click.echo(f"    Reproduced: {structural_results['n_reproduced']}/"
               f"{structural_results['n_total']}")
    click.echo(f"    Verdict: {structural_results['verdict']}")

    # 7. Overall verdict
    print_step("Computing overall verdict...")
    all_results = {
        "proximity_clustering": proximity_results,
        "similarity_decay": decay_results,
        "page_local_vocab": page_local_results,
        "first_word_repetition": fw_results,
        "structural": structural_results,
    }
    overall = compute_overall_verdict(all_results)
    all_results["overall_verdict"] = overall
    click.echo(f"    {overall['summary']}")

    # 8. Save
    print_step("Saving JSON...")
    with open(report_path, "w", encoding="utf-8") as f:
        json.dump(all_results, f, ensure_ascii=False, indent=2)
    click.echo(f"    {report_path}")

    summary = format_summary(all_results)
    with open(summary_path, "w", encoding="utf-8") as f:
        f.write(summary)
    click.echo(f"    {summary_path}")

    print_step("Writing DB table timm_test...")
    db_path = config.output_dir.parent / "voynich.db"
    if db_path.exists():
        save_to_db(all_results, db_path)
        click.echo(f"    {db_path}")
    else:
        click.echo(f"    WARN: DB not found at {db_path}")

    click.echo(f"\n{summary}")
