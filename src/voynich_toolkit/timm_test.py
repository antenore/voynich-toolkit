def compute_overall_verdict(results: dict) -> dict:
    """Synthesize evidence across all sub-tests."""
    findings: list[str] = []

    # 10a
    prox = results["proximity_clustering"]
    exact_rate = prox["observed"]["exact_match_proximity_rate"]
    z_prox = prox.get("z_score")
    if z_prox is not None and z_prox > 2:
        findings.append(
            f"Proximity clustering: {exact_rate:.0%} (z={z_prox:+.1f}). "
            "Significantly higher than null — self-citation signature PRESENT.")
    elif z_prox is not None:
        findings.append(
            f"Proximity clustering: {exact_rate:.0%} (z={z_prox:+.1f}). "
            "Not significantly above null — self-citation signature ABSENT.")

    # 10b
    corr = results["similarity_decay"].get("correlation_distance_similarity")
    if corr is not None and corr < -0.5:
        findings.append(f"Similarity decay: r={corr:+.3f} — strong, compatible with self-citation.")
    elif corr is not None:
        findings.append(f"Similarity decay: r={corr:+.3f} — weak/moderate.")

    # 10c
    plocal = results["page_local_vocab"]
    z_page = plocal.get("z_score")
    if z_page is not None and z_page > 2:
        findings.append(f"Page-local vocab: z={z_page:+.1f} — adjacent pages share more vocabulary.")
    elif z_page is not None:
        findings.append(f"Page-local vocab: z={z_page:+.1f} — not significant.")

    # 10d
    fw = results["first_word_repetition"]
    z_fw = fw.get("z_score")
    if z_fw is not None and z_fw > 2:
        findings.append(f"First-word repetition: z={z_fw:+.1f} — elevated, compatible with copying.")
    elif z_fw is not None:
        findings.append(f"First-word repetition: z={z_fw:+.1f} — not significant.")

    # 10e
    struct = results["structural"]
    n_repro = struct["n_reproduced"]
    n_total = struct["n_total"]

    # Compare with Markov (Phase 9f reproduced 1/4)
    findings.append(
        f"Structural properties: self-citation reproduces {n_repro}/{n_total} "
        f"(Markov reproduced ~1/4 in Phase 9f)."
    )

    # Summary
    signature_present = (z_prox is not None and z_prox > 2)
    struct_sufficient = (n_repro == n_total)

    if signature_present and struct_sufficient:
        summary = ("SELF-CITATION COMPATIBLE: the manuscript shows self-citation "
                   "signatures AND the mechanism reproduces all structural properties.")
    elif signature_present and not struct_sufficient:
        summary = (f"SELF-CITATION PARTIAL: self-citation signatures are present but "
                   f"the mechanism fails to reproduce {n_total - n_repro} structural "
                   f"properties. Self-citation alone is insufficient.")
    elif not signature_present:
        summary = ("SELF-CITATION SIGNATURES ABSENT: the manuscript does not show "
                   "significantly elevated proximity clustering vs null model.")
    else:
        summary = "INCONCLUSIVE"

    return {"summary": summary, "findings": findings}


