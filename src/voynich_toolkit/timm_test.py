def _modify_word(word: str, rng: random.Random) -> str:
    """Apply a random Timm-style modification to a word.

    50% glyph swap, 30% add/delete, 20% prefix modification.
    """
    if not word:
        return word

    r = rng.random()

    if r < 0.50:
        # Glyph substitution: replace one character
        if len(word) >= 1:
            pos = rng.randint(0, len(word) - 1)
            new_char = rng.choice(EVA_CHARS)
            word = word[:pos] + new_char + word[pos + 1:]

    elif r < 0.80:
        # Addition (15%) or deletion (15%)
        if rng.random() < 0.5 and len(word) > 1:
            # Delete one character
            pos = rng.randint(0, len(word) - 1)
            word = word[:pos] + word[pos + 1:]
        else:
            # Add one character
            pos = rng.randint(0, len(word))
            new_char = rng.choice(EVA_CHARS)
            word = word[:pos] + new_char + word[pos:]

    else:
        # Prefix modification: add/remove common prefix
        prefixes = ["d", "q", "o", "ch", "ok", "qo"]
        if rng.random() < 0.5:
            # Add prefix
            prefix = rng.choice(prefixes)
            word = prefix + word
        elif len(word) > 2:
            # Remove prefix if present
            for prefix in sorted(prefixes, key=len, reverse=True):
                if word.startswith(prefix):
                    word = word[len(prefix):]
                    break

    return word


def generate_self_citation_corpus(pages: list[dict], seed: int = SEED) -> list[dict]:
    """Generate a synthetic corpus using simplified self-citation.

    For each page:
    1. Seed with the first line of the real page
    2. For subsequent lines: copy words from previous 1-5 lines
    3. Apply random modifications
    4. Optionally add gallows prefix to paragraph-initial words (94%)
    """
    rng = random.Random(seed)
    simple_gallows = list("tkpf")

    synthetic_pages = []

    for page in pages:
        real_lines = page.get("line_words", [])
        if not real_lines:
            synthetic_pages.append({
                "section": page.get("section", "?"),
                "words": [],
                "line_words": [],
            })
            continue

        synth_lines: list[list[str]] = []

        # Seed with real first line
        synth_lines.append(list(real_lines[0]))

        for line_idx in range(1, len(real_lines)):
            target_len = len(real_lines[line_idx])
            if target_len == 0:
                synth_lines.append([])
                continue

            # Select source: previous 1-5 lines
            lookback = min(5, len(synth_lines))
            source_line_idx = rng.randint(
                len(synth_lines) - lookback, len(synth_lines) - 1
            )
            source_words = synth_lines[source_line_idx]

            if not source_words:
                # Fallback: use seed line
                source_words = synth_lines[0] if synth_lines[0] else ["daiin"]

            # Generate words by copying and modifying
            new_line: list[str] = []
            for w_idx in range(target_len):
                source_word = source_words[w_idx % len(source_words)]
                # 30% copy unchanged, 70% modify
                if rng.random() < 0.30:
                    new_word = source_word
                else:
                    new_word = _modify_word(source_word, rng)
                new_line.append(new_word)

            # Paragraph-initial: add gallows prefix (94% per Timm)
            # We approximate: first line of each page + every ~5th line
            if line_idx == 0 or (line_idx % 5 == 0 and rng.random() < 0.94):
                if new_line and not any(c in simple_gallows for c in new_line[0]):
                    glyph = rng.choice(simple_gallows)
                    new_line[0] = glyph + new_line[0]

            synth_lines.append(new_line)

        # Build page dict
        all_words = [w for line in synth_lines for w in line]
        synthetic_pages.append({
            "section": page.get("section", "?"),
            "words": all_words,
            "line_words": synth_lines,
        })

    return synthetic_pages


def _measure_structural_properties(pages: list[dict]) -> dict:
    """Measure the 4 key structural properties (same as Phase 9f)."""
    results = {}

    # 1. Line-end 'm' fraction
    m_total = 0
    m_line_final = 0
    for page in pages:
        for line in page.get("line_words", []):
            if not line:
                continue
            for word in line:
                m_total += word.count("m")
            last_word = line[-1]
            if last_word.endswith("m"):
                m_line_final += 1

    results["m_line_final_rate"] = m_line_final / max(1, m_total)

    # 2. Simple gallows at line[0] rate
    simple_gallows = {"t", "k", "p", "f"}
    gallows_at_start = 0
    gallows_total = 0
    for page in pages:
        for line in page.get("line_words", []):
            if not line:
                continue
            first_word = line[0]
            for ch in first_word:
                if ch in simple_gallows:
                    gallows_at_start += 1
            for word in line:
                for ch in word:
                    if ch in simple_gallows:
                        gallows_total += 1

    results["gallows_first_word_rate"] = (
        gallows_at_start / max(1, gallows_total)
    )

    # 3. Cross-boundary repeat rate
    n_boundaries = 0
    n_repeats = 0
    for page in pages:
        lines = page.get("line_words", [])
        for i in range(len(lines) - 1):
            if not lines[i] or not lines[i + 1]:
                continue
            n_boundaries += 1
            if lines[i][-1] == lines[i + 1][0]:
                n_repeats += 1

    results["cross_boundary_rate"] = n_repeats / max(1, n_boundaries)

    # 4. Section vocabulary specificity
    section_types: dict[str, set] = defaultdict(set)
    for page in pages:
        sec = page.get("section", "?")
        section_types[sec].update(page["words"])

    sections = [s for s in section_types if len(section_types[s]) >= 10]
    if len(sections) >= 2:
        overlaps = []
        for i in range(len(sections)):
            for j in range(i + 1, len(sections)):
                a = section_types[sections[i]]
                b = section_types[sections[j]]
                jaccard = len(a & b) / len(a | b) if (a | b) else 0
                overlaps.append(jaccard)
        results["section_jaccard_mean"] = float(np.mean(overlaps))
    else:
        results["section_jaccard_mean"] = None

    return results


def test_self_citation_structural(pages: list[dict],
                                   n_synth: int = N_SYNTH,
                                   seed: int = SEED) -> dict:
    """Generate synthetic self-citation corpora and compare structural properties."""
    real_measures = _measure_structural_properties(pages)

    null_measures: dict[str, list] = {k: [] for k in real_measures}

    for perm_i in range(n_synth):
        synth_pages = generate_self_citation_corpus(pages, seed=seed + perm_i)
        synth_measures = _measure_structural_properties(synth_pages)
        for k, v in synth_measures.items():
            null_measures[k].append(v)

    results = {}
    for prop, real_val in real_measures.items():
        nulls = [v for v in null_measures[prop] if v is not None]
        if not nulls:
            results[prop] = {
                "real": real_val, "reproduced": False,
                "reason": "no valid null samples",
            }
            continue

        null_mean = float(np.mean(nulls))
        null_std = float(np.std(nulls, ddof=1))
        z = _z_score(real_val, null_mean, null_std)

        reproduced = z is not None and abs(z) < 2.0
        results[prop] = {
            "real": round(real_val, 4) if real_val is not None else None,
            "null_mean": round(null_mean, 4),
            "null_std": round(null_std, 6) if null_std else None,
            "z_score": round(z, 2) if z is not None else None,
            "reproduced": reproduced,
        }

    n_repro = sum(1 for r in results.values() if r.get("reproduced"))
    n_total = len(results)

    return {
        "properties": results,
        "n_reproduced": n_repro,
        "n_total": n_total,
        "verdict": ("SELF-CITATION SUFFICIENT" if n_repro == n_total
                     else "SELF-CITATION INSUFFICIENT"),
    }


