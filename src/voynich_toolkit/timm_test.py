def test_first_word_repetition(lines: list[dict], window: int = 3) -> dict:
    """Check if first word of a line repeats as first word in previous lines."""
    n_lines = len(lines)
    n_repeated = 0
    n_tested = 0

    for i in range(window, n_lines):
        first_word = lines[i]["words"][0] if lines[i]["words"] else None
        if first_word is None:
            continue
        n_tested += 1
        # Check previous 'window' lines
        for j in range(max(0, i - window), i):
            prev_first = lines[j]["words"][0] if lines[j]["words"] else None
            if first_word == prev_first:
                n_repeated += 1
                break

    rate = n_repeated / max(1, n_tested)

    return {
        "first_word_repetition_rate": round(rate, 4),
        "n_repeated": n_repeated,
        "n_tested": n_tested,
        "window": window,
    }


def null_first_word_repetition(lines: list[dict], window: int = 3,
                                n_perms: int = N_NULL,
                                seed: int = SEED) -> dict:
    """Null: shuffle first words across lines within page."""
    rng = random.Random(seed)

    # Group lines by folio
    by_folio: dict[str, list[int]] = defaultdict(list)
    for i, line in enumerate(lines):
        by_folio[line["folio"]].append(i)

    first_words = [line["words"][0] if line["words"] else "" for line in lines]

    null_rates: list[float] = []

    for _ in range(n_perms):
        shuffled_fw = first_words[:]
        for folio, indices in by_folio.items():
            fw_in_folio = [shuffled_fw[i] for i in indices]
            rng.shuffle(fw_in_folio)
            for idx, fw in zip(indices, fw_in_folio):
                shuffled_fw[idx] = fw

        # Measure repetition rate
        n_rep = 0
        n_test = 0
        for i in range(window, len(lines)):
            fw = shuffled_fw[i]
            if not fw:
                continue
            n_test += 1
            for j in range(max(0, i - window), i):
                if shuffled_fw[j] == fw:
                    n_rep += 1
                    break

        null_rates.append(n_rep / max(1, n_test))

    return {
        "null_mean": round(float(np.mean(null_rates)), 4),
        "null_std": round(float(np.std(null_rates, ddof=1)), 6),
    }


