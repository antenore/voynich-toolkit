def test_proximity_clustering(lines: list[dict]) -> dict:
    """Timm's central claim: 91% of similar words are within 3 lines.

    For each word occurrence, find the nearest occurrence of a similar
    word (edit distance <= 2) and record the line distance.
    """
    # Build word occurrence index: word -> list of (global_line_idx)
    word_positions: dict[str, list[int]] = defaultdict(list)
    for i, line in enumerate(lines):
        for word in line["words"]:
            word_positions[word].append(i)

    # For efficiency: only consider words that appear 2+ times
    # and sample if too many types
    multi_types = [w for w, pos in word_positions.items() if len(pos) >= 2]

    # Exact-match proximity: fraction of repeated words within PROXIMITY_WINDOW
    n_within = 0
    n_total_pairs = 0
    for word in multi_types:
        positions = word_positions[word]
        for j in range(len(positions) - 1):
            dist = positions[j + 1] - positions[j]
            n_total_pairs += 1
            if dist <= PROXIMITY_WINDOW:
                n_within += 1

    exact_rate = n_within / max(1, n_total_pairs)

    # Edit-distance proximity: for each occurrence, find nearest similar
    # word (edit dist <= MAX_EDIT_DIST) and check if within window.
    # This is expensive, so sample up to 2000 word types.
    rng = random.Random(SEED)
    all_types = list(word_positions.keys())
    if len(all_types) > 2000:
        sample_types = rng.sample(all_types, 2000)
    else:
        sample_types = all_types

    n_edit_within = 0
    n_edit_total = 0
    for word in sample_types:
        positions = word_positions[word]
        # Find similar words (edit distance <= MAX_EDIT_DIST)
        for other_word, other_positions in word_positions.items():
            if other_word == word:
                continue
            if abs(len(other_word) - len(word)) > MAX_EDIT_DIST:
                continue
            ed = _edit_distance(word, other_word)
            if ed > MAX_EDIT_DIST:
                continue
            # For each pair of positions, check proximity
            for p1 in positions:
                # Find nearest position in other_positions
                # Binary search would be faster but positions are sorted
                min_dist = float("inf")
                for p2 in other_positions:
                    d = abs(p1 - p2)
                    if d < min_dist:
                        min_dist = d
                n_edit_total += 1
                if min_dist <= PROXIMITY_WINDOW:
                    n_edit_within += 1

    edit_rate = n_edit_within / max(1, n_edit_total)

    return {
        "exact_match_proximity_rate": round(exact_rate, 4),
        "exact_match_n_pairs": n_total_pairs,
        "edit_distance_proximity_rate": round(edit_rate, 4),
        "edit_distance_n_pairs": n_edit_total,
        "n_word_types": len(all_types),
        "n_multi_types": len(multi_types),
        "timm_claim": 0.91,
    }


def null_proximity_clustering(lines: list[dict], n_perms: int = N_NULL,
                               seed: int = SEED) -> dict:
    """Null: shuffle words within section, preserving line lengths."""
    rng = random.Random(seed)

    # Group lines by section (via folio -> section mapping)
    by_section: dict[str, list[int]] = defaultdict(list)
    for i, line in enumerate(lines):
        sec = line.get("section", "?")
        by_section[sec].append(i)

    null_exact_rates: list[float] = []

    for perm_i in range(n_perms):
        # Shuffle words within each section
        shuffled_words: dict[int, list[str]] = {}
        for sec, line_indices in by_section.items():
            all_words = []
            line_lengths = []
            for idx in line_indices:
                words = lines[idx]["words"]
                all_words.extend(words)
                line_lengths.append(len(words))

            rng.shuffle(all_words)

            pos = 0
            for idx, length in zip(line_indices, line_lengths):
                shuffled_words[idx] = all_words[pos:pos + length]
                pos += length

        # Measure exact-match proximity on shuffled
        word_pos: dict[str, list[int]] = defaultdict(list)
        for i in range(len(lines)):
            for word in shuffled_words.get(i, []):
                word_pos[word].append(i)

        n_within = 0
        n_total = 0
        for positions in word_pos.values():
            if len(positions) < 2:
                continue
            for j in range(len(positions) - 1):
                dist = positions[j + 1] - positions[j]
                n_total += 1
                if dist <= PROXIMITY_WINDOW:
                    n_within += 1

        null_exact_rates.append(n_within / max(1, n_total))

    return {
        "null_mean": round(float(np.mean(null_exact_rates)), 4),
        "null_std": round(float(np.std(null_exact_rates, ddof=1)), 6),
    }


