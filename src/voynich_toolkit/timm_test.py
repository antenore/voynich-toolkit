def format_summary(results: dict) -> str:
    lines: list[str] = []
    lines.append("=" * 80)
    lines.append("  PHASE 10 — Timm (2014) self-citation test")
    lines.append("=" * 80)
    lines.append("")
    lines.append("  Timm claims text was generated by copying nearby words")
    lines.append("  and modifying them. No cipher, no language — just copying.")
    lines.append("  Bold claim: 'no VMS property contradicts self-citation.'")

    # 10a
    lines.append("\n-- 10a: Proximity clustering (Timm claims 91%) --")
    prox = results["proximity_clustering"]
    obs = prox["observed"]
    lines.append(f"  Exact-match proximity rate: {obs['exact_match_proximity_rate']:.1%} "
                 f"({obs['exact_match_n_pairs']} pairs)")
    lines.append(f"  Edit-dist proximity rate:   {obs['edit_distance_proximity_rate']:.1%} "
                 f"({obs['edit_distance_n_pairs']} pairs)")
